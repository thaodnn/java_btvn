public class Circle {
    int r= 8;
    double pi = 3.14;
    void chuviC()
    {
        double cv = 2 * pi* (double)r;
        System.out.println("Chu vi hình tròn:" +cv);
    }
    void dientichC()
    {
        double dt = pi*(double)r*(double)r;
        System.out.println("Diện tích hình chữ nhật:" +dt);
    }

}
